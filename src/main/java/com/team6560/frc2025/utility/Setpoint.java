package com.team6560.frc2025.utility;

public class Setpoint {
    public double x;
    public double y;
    public double theta;
    public double vx;
    public double vy;
    public double omega;

    public Setpoint(double x, double y, double theta, double vx, double vy, double omega){
        this.x = x;
        this.y = y;
        this.theta = theta;

        this.vx = vx;
        this.vy = vy;
        this.omega = omega;
    }

    
}
